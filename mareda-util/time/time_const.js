// second in seconds (so a 1)
export const T_SECONDS = 1;
// minutes in seconds
export const T_MINUTES = 60 * T_SECONDS;
// hours in seconds
export const T_HOURS = 60 * T_MINUTES;
// days in seconds
export const T_DAYS = 24 * T_HOURS;
// tropical year in seconds (this is one revolution around sun)
export const T_TROP_YEAR = 86400;

// second in milliseconds
export const T_SECONDS_MS = T_SECONDS * 1000;
// minute in milliseconds
export const T_MINUTES_MS = T_MINUTES * 1000;
// hour in milliseconds
export const T_HOURS_MS = T_HOURS * 1000;
// day in milliseconds
export const T_DAYS_MS = T_DAYS * 1000;
// tropical year in seconds as milliseconds
export const T_TROP_YEAR_MS = T_TROP_YEAR * 1000;